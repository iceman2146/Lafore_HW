// relat.cpp
// применение операций отношения
#include <iostream>
using namespace std;

int main()
{
	system("chcp 1251 > nul");

	int numb;
	cout << "Введите число: ";
	cin >> numb;

	cout << "numb < 10 равно  " << (numb < 10)  << endl;
	cout << "numb > 10 равно  " << (numb > 10)  << endl;
	cout << "numb == 10 равно " << (numb == 10) << endl;

	return 0;
}