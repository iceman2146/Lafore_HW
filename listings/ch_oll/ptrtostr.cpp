// ptrtostr.cpp
// массив указателей на строки
#include <iostream>
using namespace std;
const int DAYS = 7;

int main()
{
	system("chcp 1251 > nul");

	// массив указателей на строки
	char* arrptrs[DAYS] = { "Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье" };

	for(int j = 0; j < DAYS; j++)    // покажем все строки
		cout << arrptrs[j] << endl;

	return 0;
}
